



function buttonAdd(){
    const btn = document.getElementById('a1-button');
    btn.style.backgroundColor = '#1DD100';
   
    const currentSeatElement = document.getElementById('current-seat');
    const currentScoreText = currentSeatElement.innerText;
    const currentSeat = parseInt(currentScoreText);
    console.log(currentSeat)

    const newSeat = currentSeat + 1;
    currentSeatElement.innerText = newSeat;

    

    const currentSeatLeft = document.getElementById('seat-left');
    const currentScoreSeat = currentSeatLeft.innerText;
    const currentLeft = parseInt(currentScoreSeat);
    console.log(currentLeft);

    const seatLeft = currentLeft - 1;
    currentSeatLeft.innerText = seatLeft;

}

